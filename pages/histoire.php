                <p class="text-center" id="text" style="border:15px solid transparent;">
                    Critique</br>
                    Red Dead Redemption 2 est très bien reçu par les critiques qui sont unanimes quant à la qualité
                    exceptionnelle du jeu. Le journaliste Logan de Jeuxvideo.com estime que : « Plus que pour son visuel
                    sidérant de beauté et son effarante perfection sonore, Red Dead Redemption II se révèle précieux
                    pour ce qu'il a à raconter et sa manière de faire évoluer de façon crédible la dimension familiale
                    de la bande, centrale dans cette suite. Tout en profitant d'un gigantesque open world vivant,
                    cohérent et parvenant à nous surprendre même après y avoir passé une cinquantaine d'heures, on se
                    retrouve pris au piège de cette histoire servie par une brochette de personnages tous mieux écrits
                    les uns que les autres. Et c'est là la force du jeu de Rockstar Games qui réussit la plupart du
                    temps à dépasser sa dimension vidéoludique en usant de son récit pour mieux ferrer le joueur afin de
                    le faire rire, frémir et de l'émouvoir. Plus qu'un jeu, Red Dead Redemption II est une épopée, née
                    des plus grands chefs-d’œuvre cinématographiques du genre, se nourrissant perpétuellement de grands
                    sentiments pour nous retourner jusqu'à un final étonnant et logique à la fois. ». </br>
                    Pour William Audureau du journal Le Monde, « Red Dead Redemption 2 donne à vivre une aventure
                    marquante dans des Etats-Unis en pleine mutation. Une œuvre fascinante, en dépit de certains
                    archaïsmes. ».</br>
                    Dans The New York Times, Peter Suderman juge que Red Dead Redemption 2 est « une véritable oeuvre
                    d'art » et qu'il s'agit probablement du jeu vidéo le plus ambitieux de l'histoire.</br>
