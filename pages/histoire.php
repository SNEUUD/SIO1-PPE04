                <h1>Résumé</h1>
                En 1899, après un braquage de ferry raté, la bande de Van der Linde est forcée de quitter Blackwater et
                d'y laisser une somme importante d'argent. Le gang - décimé par les décès des frères Davey et Mac
                Callander, ainsi que de Jenny - se réfugie dans les montagnes, et réalisant que l'industrialisation
                gagne du terrain, il est décidé de faire « un dernier braquage », qui leur rapportera suffisamment
                d'argent pour échapper aux autorités et partir en retraite. Après s'être installés près de la petite
                ville fermière de Valentine, ils braquent un train appartenant à Leviticus Cornwall, président d'un
                grand groupe industriel minier, qui répond en engageant l'agence Pinkerton pour les appréhender. Alors
                que plusieurs membres sont réunis dans un saloon, Cornwall les prend dans un guet-apens qui tourne à la
                fusillade mortelle, forçant le gang à quitter Valentine pour Rhodes, dans l'État de Lemoyne.
                Alors que la ville est rongée par la rivalité entre les familles Gray et Braithwaite, Dutch décide de
                profiter de cette opportunité en tentant d'amener les deux familles à s'affronter directement, dans
                l'espoir de pouvoir dérober un soi-disant trésor. Le gang effectue ainsi quelques missions pour les deux
                familles, ainsi que pour le shérif local, tentant de gagner leur confiance, mais rapidement, le plan
                leur retombe dessus. Pendant que des membres sont retenus dans la ville sous le feu des Gray, Jack, fils
                de John Marston, est capturé par les Braithwaite. Sean Macguire est tué dans le guet-apens ; le reste du
                gang se réunit et attaque les Braithwaite dans l'espoir de récupérer Jack, mais il a déjà été donné à
                Angelo Bronte, un homme influant de la ville de St-Denis. Le gang se relocalise à proximité de St-Denis,
                et continue à nourrir l'espoir de partir à l'étranger après un ultime coup d'éclat. </br>
                Après avoir rendu Jack en échange d'un service, Bronte semble coopératif et met le gang en contact avec
                des personnes influentes de la ville. Celui-ci finit également par les trahir en leur promettant une
                soi-disant fortune cachée dans le terminus du tramway de St-Denis. La bande se retrouve encerclée de
                policiers, après quoi Dutch capture Bronte, et finit par le tuer de sang-froid en le donnant en pâture à
                un alligator. Arthur commence à douter de l'attitude de Dutch. Dans le même temps, Arthur fait la
                connaissance de Aigle-qui-Vole, le fils du chef d'une tribu indienne aux prises avec un groupe pétrolier
                tentant de les chasser. </br>
                La bande braque une banque à Saint-Denis mais l'agence Pinkerton intervient : Hosea Matthews et le jeune
                Leonard "Lenny" Summers sont tués tandis que John est arrêté. Profitants de la diversion de leur
                compagnon Charles Smith ; Dutch, Arthur, Bill Williamson, Micah Bell et Javier Escuella sont alors
                contraints de prendre un bateau vers Cuba. Un orage détruit le navire et les cinq membres se retrouvent
                naufragés sur l'île de Guarma. Ils sont alors mêlés à un conflit local opposant Fussar, le propriétaire
                tyrannique d'une plantation de sucre, à ses esclaves. Après avoir aidé ces derniers à tuer Fussar, ils
                retournent aux États-Unis et retrouvent le reste du gang. </br>
                De retour, le gang est obligé de se relocaliser à Beaver Hollow, à proximité de la ville minière
                d'Annesburg et du village de pêcheur de Butcher Creek. La bande commence alors à se disloquer : tout
                d'abord, Molly est tuée après qu'elle a avoué avoir trahi Dutch. Certains membres prennent la fuite,
                inquiets pour leur avenir et encouragés par Arthur. Celui-ci est d'ailleurs pressé par Abigail de sortir
                John de prison, ce qu'il fait, aidé de Sadie Adler, contre la volonté de Dutch. Celui-ci finit par
                douter d'Arthur et prend Micah en bras droit à sa place. Arthur apprend aussi qu'il a contracté la
                tuberculose et qu'il est condamné. La méfiance d'Arthur à propos de Dutch est grandissante, en
                particulier car Arthur ne fait pas confiance à Micah. Dutch continue d'être obsédé par son « dernier
                braquage », et organise avec Micah l'assaut d'un train de l'armée. </br>
                À la suite d'une attaque des agents de Pinkerton contre le camp, Dutch commence à croire qu'un mouchard
                est présent parmi le groupe. Il essaie de profiter de la situation des indiens pour faire diversion et
                organiser leur fuite après l'attaque de train. Arthur perd confiance en Dutch lorsqu'il l'abandonne à
                son sort, cerné par l'Armée, alors que la bande attaque un centre pétrolier pour le compte des indiens.
                Cet abandon n'est ni le premier ni le dernier de Dutch, qui va ensuite laisser John pour mort dans
                l'attaque de train et refuser de sauver Abigail, capturée par l'agent de la Pinkerton Andrew Milton.
                Arthur et Sadie vont la sauver, apprenant par Andrew Milton que Micah est le mouchard depuis leur retour
                des Caraïbes. Abigail tue Milton et s'enfuit avec Sadie et Jack, tandis que Arthur retourne au camp
                affronter Micah. </br>
                Alors que la bande se divise entre Dutch et Micah d'un côté, et Arthur et John de l'autre, l'agence
                Pinkerton intervient et Arthur s'enfuit avec John. Le joueur a alors le choix entre retourner chercher
                l'argent de la bande ou aider John à fuir vers Abigail en retenant les agents de la Pinkerton. Dans les
                deux cas, Micah piège Arthur et Dutch intervient. Arthur convainc Dutch d'abandonner Micah, après quoi,
                si le joueur a un niveau honneur bas, Micah le tue, ou au contraire, Micah s'enfuit et Arthur succombe
                de sa maladie.
                </p>
                <p id='text'>
                <h1>Epilogue</h1>
                Durant cet épilogue, qui se déroule huit années après la mort d'Arthur Morgan, le joueur contrôle John
                Marston. Une fois les missions principales terminées, il est possible de continuer à explorer librement
                le monde et de pratiquer des activités annexes.</br>
                En 1907, John Marston et sa famille essaient de vivre honnêtement et d'oublier leur passé tumultueux.
                John se fait engager comme ouvrier dans un ranch à proximité de Strawberry, et Abigail en tant que femme
                de ménage pour le docteur de la ville. Cependant, John est obligé de reprendre les armes et sauve le
                ranch de la destruction par un gang local. Abigail, qui pense que John ne pourra jamais oublier ses
                vieilles habitudes, fini par s'enfuir avec son fils Jack, mais précise qu'elle continue de l'aimer.
                Après plusieurs mois de travail, et après avoir attiré la sympathie de M. Geddes (le gérant du ranch)
                qui va lui faciliter un prêt, John achète un ranch à proximité de Blackwater, et rencontre certains de
                ses anciens compagnons : L'Oncle, Charles et Sadie Adler. Il construit une maison avec l'aide de L'Oncle
                et Charles, tandis que Sadie l'aide à rembourser son prêt grâce à ses boulots de chasseur de prime. John
                finit aussi par demander Abigail en mariage, qui est entre-temps revenue. John, Sadie et Charles
                retrouvent la trace de Micah, caché dans les montagnes, et décident d'intervenir. Pendant</br>
                l'intervention, Sadie et Charles sont blessés, et John se retrouve seul pour affronter Micah. Dutch, qui
                venait à nouveau récemment de rejoindre Micah, intervient, et après un long face à face, finit par tirer
                sur Micah, avant de partir en silence, laissant John finir Micah. John retrouve alors le butin de
                Blackwater et l'utilise pour payer ses dettes.</br>
                Pendant le générique, on peut voir l'agent Ross, un ancien de Pinkerton travaillant pour le FBI
                désormais, observer le ranch de John, initiant les événements du premier Red Dead Redemption.
                </p>
